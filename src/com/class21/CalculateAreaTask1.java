package com.class21;

public class CalculateAreaTask1 {
	
	/*
	 * Create 1 class in which create a methods that will 
	 * calculate the area (volume) of 
	 * Rectangle 
	 * Square 
	 * Box 
	 * 
	 */
	
	public void area(int a, int b) {
		System.out.println("Area of rectange is "+(a*b));
	}
	
	
	public void area(int a) {
		System.out.println("Area of square is "+(a*a));
	}
	
	
	public void area(int a, int b, int c) {
		System.out.println("Volume of box is "+(a*b*c));
	}

}
