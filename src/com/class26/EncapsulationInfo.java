package com.class26;

public class EncapsulationInfo {
	
	/*
	 * Encapsulaton - process of wrapping variable and method in a single unit(Class) 
	 * Encapsulation is achieved in java language by class concept. 
	 * (Best example of Encapsulation is a class)
	 * 
	 * In encapsulation, the variables of a class will be hidden from other classes
	 * and can be accessed only through the methods of their current class.
	 * Therefore, it is also known as data hiding/method hidding.
	 *
	 *
	 * The main advantage of using of encapsulation is to secure the data from other
	 * methods, when we make a data private then these data only use within the
	 * class, but these data not accessible outside the class.
	 * 
	 *=================== To create a tighlty encapsulated class we need:
	 
       1. create a private data members (variables)==> it makes method hidding
       
       2. create public methods to access those variables (getters and setter)
       ==> normally we can not access with private accesss modifier inside another class or package
	 * but when we create public methods to access those variables (getters and setter)
	 * we can access private variables with our own methods in another class.
       
       ================================Benefits of Encapsulation =============================
       1 . The fields of a class can be made read-only or write-only.
       2 . A class can have total control over what is stored in its fields
	 */

}
