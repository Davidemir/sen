package com.class22;

public class MethodOverloadingDifferenceBetweenMethodOverriddingInfo {
	
	/*
	 * =================== Method Overloading vs Method Overriding =============================
	 * 
	 * 1. Same method name but different parameters within(method signature must beDIFFERENT) same class, 
	 * while in overriding same method name and same parameters(numbers, orders) - method signature MUST BE SAME
	 * 
	 * 2. Return type can be diffrent In overloading (return type is not part of
	 * method signature), while in OVERRIDING must be same 
	 * 
	 * 3. To Overload we do NOT need inheritance , but to override inheritance is MUST 
	 * 
	 * 4. Static, final, private methods can be overloaded, but CANNOT be OVERRIDEN 
	 * 
	 * 5. Constructors can be overloaded while CANNOT BE overriden 
	 * 
	 * 6. In overloading no restriction to access modifiers, 
	 * while in overrding the scope/visibility CANNOT be reduced(can be same, or increased).
	 * 
	 * 7. Overloading can be done at both static and non-static methods, 
	 * but Overriding can be done only at non-static method.
	 * 
	 * 8. The scope of overloading is within the class, 
	 * but The scope of Overriding are parent class and child class.
	 */

}
