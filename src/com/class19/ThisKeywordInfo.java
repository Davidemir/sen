package com.class19;

public class ThisKeywordInfo {
	
	/*
	 * ========================= This Keyword =============================
	 * 
	 * ==> this is a keyword in Java that refers to the only current instance/object.
	 *  
	 * ==> this is used to access current class instance members 
	 * 
	 *  Usage: 
	 * ● With variables ==> this can be used to refer current class instance variable.
	 * this.variableName --> we are reffering to the instance variables.
	 * If we have local variables and instance variables wiht same name to differentiate --. use this keyword
  
	 * ● With methods ==> this can be used to invoke current class method (implicitly). 
	 * 
	 * ● With constructors this() ==> can be used to invoke the current class constructor.
	 */

}
