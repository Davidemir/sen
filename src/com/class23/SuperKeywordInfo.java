package com.class23;

public class SuperKeywordInfo {
	
	/*
	 * super keyword in java is a reference variable which is used to refer
	 * immediate parent class object.
	 * 
	 *  
	 * ==> super. =super can be used to invoke immediate parent class method and instance variable. 
	 * 
	 * ==> super() can be used to invoke immediate parent class constructor.
	 * Using super we can call an immediate Super/Parent/Base class constructor.
	 * Everytime we create an Object of a child class --> constructor of a parent 
	 * class is getting executed with help of super keyword
	 * 
	 * super() it must be first line inside the constructor.
	 * 
	 */
	
	
	/*
	 * ============= what is the difference bewteen super. and super() ? ================
	 * 
	 * super. ==> is used to call super class variables and methods by the subclass
	 * object when they are overridden by subclass.
	 * 
	 * super() ==> is used to call super class constructor from subclass constructor.
	 */
	

}
