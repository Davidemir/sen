package com.class23;

public class FinalKeywordInfo {

	/*
	 * final is keyword identifies that no further modification can be made to
	 * classes, methods, variables
	 * 
	 * final is a non access modifier 
	 * 
	 * syntax for variables 
	   access modifier non access modifier type name;
	 * 
	 * syntax for methods 
	 * access modifier non access returnType name(){}
	 * 
	 * Final with: 
	 * == variables --> makes variable to have a constant value, you cannot
	 * change the value of that variable later in the code 
	 * We can use access modifier for final variables but it must be out of methods.(instance)
	 * 
	 * == methods --> it prevents overriding process, final methods cannot be overriden from the child class
	 * but we can overload a final method.
	 * 
	 * == classes --> prevent inheritance, class defined as final cannot have any subclasses.
	 */

}
