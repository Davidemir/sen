package com.class21;

public class PolymorphismInfo {
	
	/*
	 * Polymorphism is derived from 2 Greek words: poly and morphs. The word "poly"
	 * means many and "morphs" means forms. Polymorphism is the ability of an object
	 * to take on many forms.
	 * 
	 * Real life example of polymorphism :
	 * 
	 * Suppose if you are in class room that time you behave like a student, when
	 * you are in market at that time you behave like a customer, when you at your
	 * home at that time you behave like a son or daughter, Here one person present
	 * in different-different behaviors.
	 * 
	 * ==> Polymorphism is one of the OOPs feature that allows us to perform a
	 * single action in different ways.
	 * 
	 * ==> Polymorphism is the capability of a method to do different things based
	 * on the object that it is acting upon. An important usage of polymorphism
	 * occurs in oops is how a parent class refers to a child class object.
	 * 
	 * ==> Polymorphism can be achieved in two of the following ways:
	 * 
	 * 1. Compile time Polymorphism/Method Overloading/Static Binding/Early Binding 
	 * 2. Run time Polymorphism/Method Overriding/ Dynamic Binding
	 */


}
